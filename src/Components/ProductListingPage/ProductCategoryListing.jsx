export function productListingItem(productCategoryType) {
  let displayArray;
  switch (productCategoryType) {
    case "fresh vegetables":
      displayArray = vegetables;
      break;
    case "fresh fruits":
      displayArray = fruits;
      break;
    case "dairy bread & eggs":
      displayArray = dairyItems;
      break;
    case "cereals & breakfast":
      displayArray = cerealsBreakfast;
      break;
    case "rica atta & dalls":
      displayArray = riceWheatItems;
      break;
    case "masala & dry fruits":
      displayArray = masalaDryFruitItems;
      break;
    case "oils & ghee":
      displayArray = oilItems;
      break;
    case "fresh meat & sea food":
      displayArray = meatItems;
      break;
    case "kitchen & dining":
      displayArray = diningItems;
      break;
    case "bakery":
      displayArray = bakeryItems;
      break;
    case "chips and namkeens":
      displayArray = chipsNamkeensItems;
      break;
    case "ice cream & frozen desserts":
      displayArray = iceCreamItems;
      break;
    case "chocolates & sweets":
      displayArray = chocolatesSweetsItems;
      break;
    case "cold drinks & juices":
      displayArray = drinksJuicesItems;
      break;
    case "noodles pasta & vermi":
      displayArray = noodlesPastaItems;
      break;
  }
  return displayArray;
}
